#ifndef FLOCK_H__
#define FLOCK_H__

#include <ngl/Vec3.h>
#include <vector>
#include <boid.h>
#include <behaviour.h>

/// @file flock.h
/// @brief a class for creating boids
/// @author original author Eligijus Titas
/// @version 1.0
/// @date 15/05/15
/// @class Flock
/// @brief main boid creation class

class Flock
{
public:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief ctor for Flock class
  //----------------------------------------------------------------------------------------------------------------------
  Flock();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief dtor for the Flock class
  //----------------------------------------------------------------------------------------------------------------------
  ~Flock();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief method to add boids into a given vector
 /// @param [in] _boidNum the amount of boids to create
 /// @param [in] _boidList the vector to add the boinds into
  //----------------------------------------------------------------------------------------------------------------------
  void boidFlock(int _boidNum, std::vector<Boid *> &_boidList, double _radius);
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief method to check collison of boids
 /// @param [in] _boidList the boid list
  //----------------------------------------------------------------------------------------------------------------------
  void collisionBoids(std::vector<Boid *> _boidList);
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief method to clear the give boid list
 /// @param [in] _boidList the boid list
  //----------------------------------------------------------------------------------------------------------------------
  void flockClear(std::vector<Boid*> &_boidList);
private:
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief variable for a boid pointer
  //----------------------------------------------------------------------------------------------------------------------
  Boid *m_boid;
};
#endif // FLOCK

